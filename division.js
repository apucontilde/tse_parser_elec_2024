// ORGANIZA LAS PROVINCIAS
function divisionProvincias() {
    const groupedData = DIVISION.reduce((accumulator, currentItem) => {
        const key = currentItem.n1;

        if (!accumulator[key]) {
            accumulator[key] = [];
        }
        accumulator[key].push(currentItem);
        return accumulator;
    }, {});
    const groupedArray = Object.values(groupedData);
    return groupedArray
}

// ARREGLO DE PROVINCIAS
function arrProvincias(data) {
    const uniqueObjectsSet = new Set();
    data.forEach((innerArray) => {
        innerArray.forEach((item) => {
            const newObj = { n1: item.n1, d1: item.d1 };
            uniqueObjectsSet.add(JSON.stringify(newObj));
        });
    });
    const uniqueObjectsArray = Array.from(uniqueObjectsSet).map((jsonString) => JSON.parse(jsonString));
    return uniqueObjectsArray;
};

